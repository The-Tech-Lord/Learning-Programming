public class PrimitiveTypes {
	public static void main(String[] args) {
		byte age = 30; // Holds 1 byte of data
		short fuck = 109; // Holds 2 bytes of data
		int aaaaaaa = 258_678_987; // Holds 4 bytes of data
		long views = 3_123_456_789L; // Holds 8 bytes of data, must end with l or L to signify it's
									 // of type long
		float price = 10.99F; // Holds 4 bytes of data, can hold 6-7 decimal points, need to put
							  // f or F to signify it's of type float
		double fuckPrices = 18.99999999999; // Holds 8 bytes of data
		char letter = 'A'; // Holds 2 bytes of data
		boolean isEligible = false; // aaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaa
	}
}
