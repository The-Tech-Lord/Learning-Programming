from math import *

#You can convert numbers into strings if you want! This is useful for printing numbers with strings
my_num = -5
print(str(my_num) + " is my favorite number")
#You can get the absolute value
print(abs(my_num))
#You can also use exponents
print(pow(3, 9))
#You can get which value is greater; Using min() prints out the mallest value
print(max(5, 9))
#You can round values
print(round(3.5))

#With the math imported there's alot more!
#floor() will round a number down no matter what
print(floor(3.7))
#ceil() will round a number up no matter what
print(ceil(3.7))
#You can also get the square root of something using sqrt()
print(sqrt(3.7))